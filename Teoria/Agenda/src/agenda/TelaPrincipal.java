    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        Pessoa p1 = new Pessoa();
        p1.setNome(jTextField1.getText());
        p1.setSobrenome(jTextField2.getText());
        p1.setApelido(jTextField3.getText());
        p1.setCelular(jTextField4.getText());
        p1.setTipoAmizade(jComboBox1.getSelectedItem().toString());
        
        Email e1 = new Email();
        e1.setEndereco(jTextField5.getText());
        if(jRadioButton1.isSelected()){
            e1.setTipo("Pessoal");
        }else if(jRadioButton2.isSelected()){
            e1.setTipo("Profissional");
        }
        
        p1.emails.add(e1);
        
        System.out.println("Infos do Objeto:");
        System.out.println("Nome: " + p1.getNome());
        System.out.println("Sobrenome: " + p1.getSobrenome());
        System.out.println("Apelido: " + p1.getApelido());
        System.out.println("Celular: " + p1.getCelular());
        System.out.println("Tipo de Amizade: " + p1.getTipoAmizade());
        System.out.println("Email: " + p1.emails.get(0).getEndereco());
    }//GEN-LAST:event_jButton5ActionPerformed


